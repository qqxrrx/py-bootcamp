# exponentiation
print(2**3)
print(4**8)
print(49**0.5)

# remainder/modulo
# just return the remainder of this division
# mostly used if number is even (divisible by 2 with 0 remainder) or odd
print(10 % 3)
print(25 % 4)

print(10 / 3)
# using // will give the value of the division without the float
print(10 // 3)
